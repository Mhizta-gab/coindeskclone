            <h2>Paying the Global Workforce</h2>
            <p>Over the past decade, and especially since COVID, more companies have embraced cross-border hiring, tapping into freelance talent across emerging markets. From AI labeling firms to customer support platforms, global teams have become a cornerstone of the digital economy. While finding the right talent may be easier than ever thanks to a larger playing field, paying that talent is still fraught with friction.</p>
            <p>Cross-border transactions often come with high fees, typically anywhere between 5% and 10%, and long delays. For workers living paycheck to paycheck, waiting several days for funds to clear can be the difference between making rent or not. Worse, in many countries with volatile currencies and high inflation, the value of those earnings can diminish before they’re even spent. Meanwhile, the lack of financial infrastructure in developing regions makes cashing out those payments a logistical nightmare.</p>
            <p>As Zach Abrams, CEO of Bridge, succinctly put it: “Cross-border payments are broken — slow, expensive, and inaccessible. This partnership proves there’s a better way. We’re proud to enable global payouts that work at scale, in real-time.”</p>

            <h2>A Solution Through Partnership: Airtm + Bridge + Stellar</h2>
            <p>Airtm’s story is a case study in product evolution. Originally launched to help Latin Americans protect their savings by converting local currencies into stable digital dollars, the company has since grown into a robust wallet platform supporting both remittances and enterprise payouts. That second use case, business-to-person (B2P) payouts, is where things have accelerated.</p>
            <p>The collaboration with Bridge brought new momentum. By enabling businesses to send dollar-denominated payouts through ACH, SEPA, or wire transfers, which Bridge converts into USDC, companies can now reach Airtm wallets without requiring their contractors to have a U.S. bank account. This on-ramp is simple and familiar for enterprises, while the crypto-related mechanics happen seamlessly in the background.</p>
            <p>Once converted to USDC, those funds move over the Stellar network’s fast and low-cost blockchain and land in Airtm wallets almost instantly. But the real power lies in the last mile: thanks to the Stellar network’s global anchors, Airtm users across the globe can access more than 500 local payout methods, including bank transfers, mobile money, and even cash pickup through outlets like MoneyGram.</p>

            <p>Two models make this system particularly flexible. In a 1st party setup, businesses open Airtm Enterprise accounts, fund them through Bridge, and distribute payments directly to users. In a 3rd party model, companies send funds to pre-assigned virtual accounts, and payouts are handled automatically via Bridge and Stellar. Either way, the result is the same: global workers receive stable, dollar-based payments they can trust, in addition to having the ability to cash out when and how they want.</p>

            <p>Rubén Galindo Steckel, CEO of Airtm, captured the real-world impact of the system: “This collaboration delivers a real, working solution to one of the hardest problems in global finance: real-time payouts to workers navigating currency instability.”</p>

            <h2>Real-World Impact: A System That Delivers</h2>
            <p>Since the integration went live in March 2024, the numbers have been nothing short of remarkable. More than 250,000 recipients across all seven continents have received payouts through Airtm’s system, with over 1.2 million successful transactions. The average cost savings for enterprise senders range from 20% to 25%. Those considerable savings can be reinvested into higher contractor wages or improved operations.</p>

            <p>What’s even more striking is where this system is seeing the most traction. Argentina has seen close to $9 million. Egypt has benefited from over $6.7 million. And that’s just the beginning. Airtm has also facilitated payments in Turkey, Pakistan, and even in mature economies like the U.S., Canada, and the U.K.</p>

            <p>These aren’t just remittance corridors. They’re pipelines for labor-intensive, knowledge-based work like AI data labeling, customer support, and outsourced back-office operations. Industries that thrive on speed, scale, and accuracy. With more global startups building distributed teams from day one, the demand for stablecoin-powered payout infrastructure is only going to grow.</p>

            <p>One company using the integration to great effect is Sapien, a platform for crowdsourced AI training data. “We’re ensuring that our contributors—no matter where they are in the world—get paid faster and with fewer barriers,” said Sapien CEO Rowan Stone. “This is a crucial step in our mission to build a more inclusive and efficient future for AI-driven work.”</p>

            <h2>Why It Works: The Unique Value of Stellar</h2>
            <p>At the core of this solution is Stellar, one of the best blockchains for payments. Unlike general-purpose networks that prioritize programmability or NFTs, Stellar is laser-focused on financial infrastructure. It supports near-instant settlement, minimal fees, and interoperability with both crypto and fiat systems.</p>

            <p>One of Stellar’s greatest assets is its anchor network: a system of regulated financial institutions and fintechs that provide on-ramps and off-ramps around the world. This includes MoneyGram’s cash pickup points, local banks, mobile money providers, and many more. For a wallet like Airtm, this means users aren’t stuck in crypto. They can convert their digital dollars into local cash or currency in a way that’s fast, legal, and trustworthy.</p>

            <p>And thanks to its integration with Bridge, Stellar can now support the entire payout pipeline, from traditional banking rails to stablecoin issuance to real-world delivery. It’s a layered system that feels seamless to the end user and powerful to the businesses behind it.</p>

